    <h3 class="text-center text-main border-bottom mb-3">Quick Setup Guide To Colibri for Homeowners</h3>
    <p>
        <strong class="text-main">Narrow Your Search:</strong>
        Colibri search function is very powerful, allowing you to limit your search by geographic
        location, type of project, and more. Plus, you can search for a specific item (like “classic kitchen design”)
        and find
        thousands of photos that match.
    </p>

    <p>
        <strong class="text-main">Share Ideas:</strong>
        When you find a photo you want to show your spouse, a friend, or your designer, use one of Colibri
        sharing options to send it to them. In addition to saving it to your Ideabooks, you can email it, send a link to
        the
        page, or share it on Facebook and Twitter.
    </p>
    <p>
        <strong class="text-main">Ask questions:</strong>
        As you find contractors that you like on Houzz, you might have some questions for them. Sometimes
        this is about a photo they’ve posted, a general question about the remodeling process, or a specific question
        about
        your situation.
    </p>
    <p>
        <strong class="text-main">Leave a review</strong>
        After you’ve conquered your remodel project, make sure you write a review for your contractor on
        their profile. Be thorough and honest; share what you liked and didn’t like about working with the company.
        Imagine
        writing this to your past self and answer any questions you had during the dreaming phase of the project. This
        will
        go a long way towards helping your neighbors find the perfect contractor for their own project.
